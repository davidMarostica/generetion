// variaveis (var e let) var = tem que colocar o valor depois
// let = pode declarar e atribuir valor depois
// const = valor constante, não pode mudar

let quantidade = 10
let altura = 1.87
let nome = "David Aparecido da Silva"
let resposta = true

// Exemplo - Tipagem Fraca | Fracamente Tipada | Tipagem Dinâmica
// JavaScript é uma linguagem de tipagem fraca, ou seja, você não precisa declarar o tipo da variável.
// O tipo é definido automaticamente com base no valor atribuído à variável.    

quantidade = "Hollo World"; // redeclarar a variável com outro tipo de dado

console.log(quantidade)
console.log(altura)
console.log(nome)
console.log(resposta)

